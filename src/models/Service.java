package models;


public class Service {
    private String servico;
    private int price;
    
    public String getService() {
        return servico;
    }

    public void setService(String servico) {
        this.servico = servico;
    }
    
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
